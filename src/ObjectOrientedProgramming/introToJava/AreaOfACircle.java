package IntroToJava;

public class AreaOfACircle {
    public static void main(String[] args) {
        int radius = 7;
        double areaOfCircle = Math.pow(Math.PI*radius, 2);
        System.out.println("The area of a circle with a radius of " + radius + " is " + areaOfCircle);
    }
}
